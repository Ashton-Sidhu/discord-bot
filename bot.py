import discord
import os
import asyncio
import requests

class MyClient(discord.Client):
    async def on_ready(self):
        print('Logged on as {0}!'.format(self.user))

    async def on_message(self, message):
        if message.author == self.user:
            return

        if message.content.startswith('!ping'):
            msg = await message.channel.send('pong!')
            await asyncio.sleep(10)
            await msg.delete()

        if message.content.startswith('!ross'):
            msg = await message.channel.send('RACHEL!')
            await asyncio.sleep(10)
            await msg.delete()

        if message.content.startswith('!season'):
            response = requests.get("http://site.api.espn.com/apis/site/v2/sports/basketball/nba/scoreboard")

            # How to print key of a dictionary: .keys()
            for items in response.json()["events"]:
                for competition in items["competitions"]:
                    team1 = competition["competitors"][0]["team"]["shortDisplayName"]
                    team2 = competition["competitors"][1]["team"]["shortDisplayName"]

                    output_string = team1 + " vs. " + team2
                    
                    await message.channel.send(output_string)

            msg = await message.channel.send(response.json()["season"])
            msg = await message.channel.send(response.json()["leagues"][0]["season"]["type"]["name"])


intents = discord.Intents.default()
client = MyClient(intents=intents)
client.run(os.environ["SIDHULABS_DISCORD_TOKEN"])


